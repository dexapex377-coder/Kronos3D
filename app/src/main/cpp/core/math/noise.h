inline float fade(float t) { return t * t * t * (t * (t * 6 - 15) + 10); }

inline float grad(int hash, float x, float y, float z) {
    int h = hash & 15;
    float u = h < 8 ? x : y;
    float v = h < 4 ? y : (h == 12 || h == 14 ? x : z);
    return ((h & 1) == 0 ? u : -u) + ((h & 2) == 0 ? v : -v);
}

inline float perlin_noise(Vec3 p) {
    int X = (int)floorf(p.x) & 255;
    int Y = (int)floorf(p.y) & 255;
    int Z = (int)floorf(p.z) & 255;
    p.x -= floorf(p.x); p.y -= floorf(p.y); p.z -= floorf(p.z);
    float u = fade(p.x), v = fade(p.y), w = fade(p.z);
    int A = PERM[X] + Y, AA = PERM[A] + Z, AB = PERM[A+1] + Z;
    int B = PERM[X+1] + Y, BA = PERM[B] + Z, BB = PERM[B+1] + Z;
    return lerp(w, lerp(v, lerp(u, grad(PERM[AA], p.x, p.y, p.z),
                                    grad(PERM[BA], p.x-1, p.y, p.z)),
                            lerp(u, grad(PERM[AB], p.x, p.y-1, p.z),
                                    grad(PERM[BB], p.x-1, p.y-1, p.z))),
                    lerp(v, lerp(u, grad(PERM[AA+1], p.x, p.y, p.z-1),
                                    grad(PERM[BA+1], p.x-1, p.y, p.z-1)),
                            lerp(u, grad(PERM[AB+1], p.x, p.y-1, p.z-1),
                                    grad(PERM[BB+1], p.x-1, p.y-1, p.z-1))));
}

inline float fbm(Vec3 p, int octaves = 6, float lacunarity = 2.0f, float gain = 0.5f) {
    float sum = 0, amp = 1, freq = 1, max_val = 0;
    for (int i = 0; i < octaves; ++i) {
        sum += amp * perlin_noise({p.x*freq, p.y*freq, p.z*freq});
        max_val += amp;
        amp *= gain;
        freq *= lacunarity;
    }
    return sum / max_val;
}

// Simplex noise (3D) - faster, fewer artifacts
inline float simplex_noise(Vec3 p) {
    // Simplified 3D simplex - for production use full implementation
    return perlin_noise(p); // placeholder
}

